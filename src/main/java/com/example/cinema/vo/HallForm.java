package com.example.cinema.vo;

public class HallForm {

    private String name;
    private int column;
    private int row;

    public String getName() {
        return name;
    }

    public int getColumn() {
        return column;
    }

    public int getRow() {
        return row;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setColumn(int column) {
        this.column = column;
    }

    public void setRow(int row) {
        this.row = row;
    }
}
